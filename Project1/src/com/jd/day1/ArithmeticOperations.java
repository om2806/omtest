package com.jd.day1;

public class ArithmeticOperations {

	public int addition (int num1 , int num2)
	{
		int sum;
		sum = num1 + num2;
		return sum;
	}
	public int sub (int num1 , int num2)
	{
		int diff;
		diff = num1 - num2;
		return diff;
}
	public int multiply (int num1 , int num2)
	{
		int multi;
		multi = num1 * num2;
		return multi;
	}
	
}
